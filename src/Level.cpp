Level::Map Level::load_level(int level_number){

  std::vector<std::vector<int>> grid{};

  Map map{};

  switch (level_number){
    case(0):
      grid = training_1;
      map.player_position = {96,128};
      break;
  };

  for(auto& row : grid){
    std::vector<bool> level_row;
    for(auto& tile_id : row){
      if(tile_id > 10){
        level_row.push_back(true);
      } else {
        level_row.push_back(false);
      }
    }
    map.tile_map.push_back(level_row);
  }

  return map;
};
